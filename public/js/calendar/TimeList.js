
	function TimeList() {
	
		this["00:00:00"] = "12:00 AM";
		this["00:30:00"] = "12:30 AM";
		
		for ( var i = 1; i < 10; i++ ) {
			this["0"+i+":00:00"] = "0"+i+":00 AM";
			this["0"+i+":30:00"] = "0"+i+":30 AM";
		}
		
		this["10:00:00"] = "10:00 AM";
		this["10:30:00"] = "10:30 AM";
		this["11:00:00"] = "11:00 AM";
		this["11:30:00"] = "11:30 AM";
		this["12:00:00"] = "12:00 PM";
		this["12:30:00"] = "12:30 PM";				
		
		for ( var i = 1; i < 10; i++ ) {
			this[(i+12)+":00:00"] = i+":00 PM";
			this[(i+12)+":30:00"] = i+":30 PM";
		}
		
		for ( var i = 10; i < 12; i++ ) {
			this[(i+12)+":00:00"] = i+":00 PM";
			this[(i+12)+":30:00"] = i+":30 PM";
		}
	
	}